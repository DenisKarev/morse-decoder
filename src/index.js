const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    // write your solution here
    const letterSize = 10;
    const words = expr.split('**********').map(w => {
        let temp = [];
        for (let i = 0; i < w.length / letterSize; i++) {
            temp.push(MORSE_TABLE[w.slice(i * letterSize, (i + 1) * letterSize)
                .replace(/00/g, '').replace(/10/g, '.').replace(/11/g, '-')]);
        }
        return temp.join('')
    });
    return words.join(' ')
}

module.exports = {
    decode
}